/**
 * Generates frontend/src/defaultData.js from docs/*.csv.
 * Run from repo root: node scripts/generate-default-data.js
 */

import { readFileSync, writeFileSync } from 'fs'
import { resolve } from 'path'

const MONTH_NAMES = ['jan', 'feb', 'mar', 'apr', 'may', 'jun', 'jul', 'aug', 'sep', 'oct', 'nov', 'dec']

function parseCSVLine(line) {
  const cells = []
  let i = 0
  while (i < line.length) {
    if (line[i] === '"') {
      let cell = ''
      i++
      while (i < line.length) {
        if (line[i] === '"') {
          i++
          if (line[i] === '"') { cell += '"'; i++ }
          else break
        } else cell += line[i++]
      }
      cells.push(cell.trim())
      if (line[i] === ',') i++
    } else {
      let cell = ''
      while (i < line.length && line[i] !== ',') cell += line[i++]
      cells.push(cell.trim())
      if (i < line.length) i++
    }
  }
  return cells
}

function parseCSV(content) {
  const lines = content.split(/\r?\n/).map((l) => l.trim()).filter(Boolean)
  if (lines.length < 2) return { headers: [], rows: [] }
  const headers = parseCSVLine(lines[0])
  const rows = []
  for (let i = 1; i < lines.length; i++) {
    const cells = parseCSVLine(lines[i])
    const row = {}
    headers.forEach((h, idx) => { row[h] = cells[idx] ?? '' })
    rows.push(row)
  }
  return { headers, rows }
}

function parseDate(s) {
  s = (s || '').trim()
  if (!s) return ''
  if (/^\d{4}-\d{2}-\d{2}$/.test(s)) return s
  const m = s.match(/^(\d{1,2})-([A-Za-z]{3})-(\d{2})$/)
  if (m) {
    const [, day, monthStr, yy] = m
    const month = MONTH_NAMES.indexOf(monthStr.toLowerCase())
    if (month === -1) return s
    const year = 2000 + parseInt(yy, 10)
    return `${year}-${String(month + 1).padStart(2, '0')}-${String(parseInt(day, 10)).padStart(2, '0')}`
  }
  return s
}

function parseNum(val) {
  if (val === '' || val == null) return null
  const v = String(val).trim().replace(/%/g, '').replace(/,/g, '')
  if (v === '' || v.toLowerCase() === '#n/a') return null
  const n = Number(v)
  return Number.isFinite(n) ? n : null
}

const repoRoot = resolve(process.cwd())
const docs = resolve(repoRoot, 'docs')
const frontendSrc = resolve(repoRoot, 'frontend', 'src')

// Line chart
const lineCsv = readFileSync(resolve(docs, 'Line Chart Data.csv'), 'utf8')
const { rows: lineRows } = parseCSV(lineCsv)
const DEFAULT_CHART_DATA = lineRows.map((r) => ({
  date: parseDate(r.Date || r.date),
  ytd: parseNum(r.YTD || r.ytd),
  sp500: parseNum(r['S&P 500'] || r.sp500),
})).filter((d) => d.date)

// Pie
const pieCsv = readFileSync(resolve(docs, 'Pie Chart Data.csv'), 'utf8')
const { rows: pieRows } = parseCSV(pieCsv)
const DEFAULT_ALLOCATION = pieRows.map((r) => ({
  label: (r[''] || r.Label || r.label || '').trim() || (r['Allocation %'] ? 'Unknown' : ''),
  weight: parseNum(r['Allocation %'] || r.Weight || r.weight) ?? 0,
  color: (r.Color || r.color || '#6b7280').trim(),
})).filter((d) => d.label)

// Positions
const posCsv = readFileSync(resolve(docs, 'Top Positions Data.csv'), 'utf8')
const { rows: posRows } = parseCSV(posCsv)
const DEFAULT_POSITIONS = posRows.map((r) => ({
  company: (r.Company || r.company || '').trim(),
  ticker: (r.Ticker || r.ticker || '').trim(),
  allocation: parseNum(r['Allocation %'] || r.allocation) ?? 0,
  category: (r.Category || r.category || '').trim(),
})).filter((d) => d.company && d.ticker)

const out = `/**
 * Default/dummy data for the dashboard (from docs/*.csv).
 * Generated by: node scripts/generate-default-data.js
 */

export const DEFAULT_CHART_DATA = ${JSON.stringify(DEFAULT_CHART_DATA, null, 2)}

export const DEFAULT_ALLOCATION = ${JSON.stringify(DEFAULT_ALLOCATION, null, 2)}

export const DEFAULT_POSITIONS = ${JSON.stringify(DEFAULT_POSITIONS, null, 2)}
`

writeFileSync(resolve(frontendSrc, 'defaultData.js'), out, 'utf8')
console.log('Wrote frontend/src/defaultData.js')
console.log('  DEFAULT_CHART_DATA:', DEFAULT_CHART_DATA.length, 'rows')
console.log('  DEFAULT_ALLOCATION:', DEFAULT_ALLOCATION.length, 'slices')
console.log('  DEFAULT_POSITIONS:', DEFAULT_POSITIONS.length, 'positions')